const { Schema, model } = require('mongoose');

const approvedUserSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    role: {
        type: String,
        required: true
    }
}, { timestamps: true });

module.exports = model('ApprovedUser', approvedUserSchema);